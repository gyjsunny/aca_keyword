                <h2 class="h2title">KEYWORD DICTIONARY OF CONTEMPORARY ART </h2>
                <div class="stext">
                    In response to the global circumstances brought on by the pandemic and to sustain the flow of cultural and artistic exchanges, the Association of Visual Arts in Taiwan (AVAT) launched a collaborative project titled "Keyword Dictionary of Asian Contemporary Art" in 2021. In 2024, to better capture its expanded entries and meanings, the project was renamed "Keyword Dictionary of Contemporary Art." This initiative emphasizes interdisciplinary experimental writing, images, sounds, and research by artists. By fostering collaborative discussions among artists from various regions, the project seeks to define the vocabularies of our time, which are subsequently reinterpreted through images, sounds, and different media, culminating in a dictionary that reflects the current era and depicts the landscape of contemporary art.<br><br>

                    This year, the project enters its second phase. Themed "Forbidden Words," AVAT invites four artists to participate in the project, along with a commissioned essay. The initiative aims to explore the emergence of "forbidden words" and the underlying censorship through artistic means. Through the artists' confronting censorship, disrupting the system of language creation, or reimagining these processes, the project seeks to reveal the power dynamics associated with the forbidden words. Additionally, the project employs a digital showcase of archives and writing workshops to serve as both a reflection on history and a caution in the contemporary time: forbidden words go beyond particular times and locations, continuously emerging with societal taboos and authoritarian rule. Moreover, forbidden words symbolize humanity's longing for freedom, underscoring that freedom and democracy require careful consideration in every era.<br><br><br><br>


                    因應疫情期間的世界局勢並維持藝文交流活動的動能，2021年起在台灣視覺藝術協會的支持下，蔡秉儒與李奎壁共同發起「亞洲藝術關鍵字共創計畫」。2024年因詞條與語意上的擴充，為了更適當地回應計畫內容，更名為「藝術關鍵字共創計畫（Keyword Dictionary of Contemporary Art）」。在本計畫中，著重於藝術家跨域的實驗性書寫、影像、聲音與研究，藉由不同地域藝術家們的共同討論，尋找出屬於這個時代的語彙，進而以影像、聲音與不同的媒材重新進行詮釋，集結為一部共同指向時代，並描繪出當代藝術面貌的字典。<br><br>

                    今年度為第二期計畫，以「禁語」為題，邀請了四位藝術工作者參與創作，以及一篇專文撰稿。禁語代表著被禁止使用的關鍵字，可能為政治因素或社會倫理規範，而這些特別被匡列的特殊字彙，同時也反映了不同社會框架中的審查制度。<br><br>

                    而「禁語」意謂著「被禁止使用的語言」、「被禁止傳播的思想」或「被禁止提及的關鍵字」，除了透過被禁止使用的原因，以及傳播的語言、知識與詞彙可以得知該社會以及社群的禁忌之外，因為禁止使用而出現的審查機制，同時也指出了國家權力運作的邏輯。在Jason Q. Ng的《微博不能說的關鍵詞》 一書中，認為「某個關鍵詞沒有在微博上被封鎖，並不表示它沒有遭到審查。書中那些未遭封鎖的詞彙即便有搜尋結果出現，它們仍有可能是經過嚴密審查的。」也因此「禁語」僅只是表現網域或是社群中的現象，他指出的其實是國家監控系統以及服務於斯的意識形態。<br><br>

                    德籍藝術家Anna Ehrenstein以德國社會中對於穆斯林移民的審查，發展迷因式的抵抗策略；台灣藝術家阮柏遠則以網路遊戲「魔獸世界」中對於遊戲圖像或言論的審查、遮蔽機制，協同AI生成一部以其中NPC為主角的小說；李紫彤則藉Me too事件反思，與心理師合作探討權勢性騷擾與自戀型虐待的關係；印尼籍目前在美國活動的Lisistrata Lusandiana，則帶領書寫工作坊，討論日惹當地無所不在的城市監視系統，並以影像的方式呈現。<br><br>

                    本計畫以「禁語」的出現以及背後的審查機制出發，邀請藝術家透過對於審查機制的擾動、語言生產系統的干擾或是再思考，使人們意識到禁語背後的權力運作系統。同時，透過檔案的數位呈現計畫及寫作工作訪，作為對於過去的回望以及今日的警醒：禁語並非發生在特定時空的事件，而是永遠伴生於社會禁忌或集權統治的另外一面，禁語同時也體現了人們對於自由的渴望，而自由與民主是每個時代都必須謹慎以對的課題。
